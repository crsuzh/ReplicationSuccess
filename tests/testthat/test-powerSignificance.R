test_that("numeric test for powerSignificance(): 1", {
    expect_equal(object = powerSignificance(zo = qnorm(p = 1 - 0.05 / 2),
                                            c = 1, level = 0.05,
                                            alternative = "two.sided"),
                 expected = 0.5,
                 tol = 0.0001)
    expect_equal(object = powerSignificance(zo = qnorm(p = 1 - 0.0056 / 2),
                                            c = 1, level = 0.05,
                                            alternative = "two.sided"),
                 expected = 0.791,
                 tol = 0.001)

})


